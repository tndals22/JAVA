package tut01;

public class AssigEx {
	public static void main(String[] args) {
		// 대입연산자

		int num = 10; // +1

		// 증감 연산자
		++num;
		num++;

		// 대입 연산자
		num = num + 1;
		num += 1;

		num = num + 2;
		num += 2;

		num = num * 10;
		num *= 10; // 중복된 값을 제거

		System.out.println(num);

	}

}
